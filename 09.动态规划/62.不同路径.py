# 62.不同路径
# 二维网格中的机器人只能向下或者向右移动。机器人试图达到网格的右下角。问总共有多少条不同的路径？
# 经典多维动态规划问题
# 假设状态dp[i][j]表示从起点(0,0)到达(i,j)的路径数量，那么状态转移方程为：
# dp[i][j] = dp[i-1][j] + dp[i][j-1],因为机器人要不是从上面(i-1,j)走下来，要不就是从左边(i,j-1)走过来，所以路径数量等于这两者的和。
class Solution:
    def uniquePaths(self, m: int, n: int) -> int:
        # 1. 初始化 m 行 n 列的二维数组，默认全是 1
        # 这样第一行和第一列的边界条件天然就是 1 了
        dp = [[1] * n for _ in range(m)]

        # 2. 从第 1 行第 1 列开始遍历（跳过边界）
        for i in range(1, m):
            for j in range(1, n):
                # 状态转移：当前格子的路径数 = 上方格子 + 左方格子
                dp[i][j] = dp[i - 1][j] + dp[i][j - 1]
        
        # 3. 返回右下角格子的路径数
        return dp[m - 1][n - 1]

